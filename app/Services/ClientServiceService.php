<?php

namespace App\Services;

use App\Repositories\ClientServiceRepository;

class ClientServiceService extends BaseService
{
    public function __construct(ClientServiceRepository $repository)
    {
        $this->repository = $repository;
    }

    // column constants
    public function clientId()
    {
        return $this->repository->clientId();
    }

    public function serviceId()
    {
        return $this->repository->serviceId();
    }

    public function status()
    {
        return $this->repository->status();
    }

    public function createdBy()
    {
        return $this->repository->createdBy();
    }

    public function updatedBy()
    {
        return $this->repository->updatedBy();
    }

    public function deletedBy()
    {
        return $this->repository->deletedBy();
    }
    // functions
}
