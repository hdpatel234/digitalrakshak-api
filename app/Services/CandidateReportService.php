<?php

namespace App\Services;

use App\Models\Candidate;
use Barryvdh\DomPDF\Facade\Pdf;
use Illuminate\Support\Facades\Storage;
use Illuminate\Support\Facades\Log;

class CandidateReportService
{
    public function __construct(protected ConfigurationService $configurationService) {}
    public function generateForCandidate(Candidate $candidate)
    {
        try {
            $candidate->load([
                'candidateServices.service',
                'candidateServices.serviceData.field',
            ]);

            $configs = $this->configurationService->getValues([
                'pdf_header_content',
                'pdf_footer_content',
                'pdf_page_size',
                'pdf_orientation'
            ]);

            $headerContent = $configs['pdf_header_content'] ?? '<h1>Candidate Services Report</h1><p>Candidate ID: {{ $candidate->id }}</p><p>Date: {{ date("Y-m-d H:i:s") }}</p>';
            $footerContent = $configs['pdf_footer_content'] ?? 'Generated by DigitalRakshak';
            $pageSize = $configs['pdf_page_size'] ?? 'a4';
            $orientation = $configs['pdf_orientation'] ?? 'portrait';

            $pdf = Pdf::setOptions(['isHtml5ParserEnabled' => true, 'isRemoteEnabled' => true])
                ->loadView('pdf.candidate_report', [
                    'candidate' => $candidate,
                    'headerContent' => $headerContent,
                    'footerContent' => $footerContent,
                ])->setPaper($pageSize, $orientation);

            $ownerPassword = \Illuminate\Support\Str::random(16);
            $pdf->setEncryption('', $ownerPassword, ['print']);

            $fileName = 'reports/Candidate_' . $candidate->id . '_' . time() . '.pdf';

            Storage::disk('local')->put($fileName, $pdf->output());

            Log::info("Generated PDF report for Candidate ID {$candidate->id} at {$fileName}");

            return $fileName;
        } catch (\Exception $e) {
            Log::error("Failed to generate PDF report for Candidate ID {$candidate->id}: " . $e->getMessage());
            return false;
        }
    }
}
